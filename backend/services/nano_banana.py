import logging

from google import genai
from google.genai import types

from config import settings

logger = logging.getLogger(__name__)


def _detect_mime(data: bytes) -> str:
    """Detect image MIME type from magic bytes, defaulting to image/png."""
    if data[:3] == b"\xff\xd8\xff":
        return "image/jpeg"
    if data[:4] == b"RIFF" and data[8:12] == b"WEBP":
        return "image/webp"
    if data[:4] == b"GIF8":
        return "image/gif"
    return "image/png"


def _generate_image(
    client: genai.Client,
    model: str,
    contents: list,
    aspect_ratio: str,
    image_size: str,
) -> bytes:
    """Call Gemini image generation with automatic fallback on 400.

    Tries: requested size → 2K → 1K → no size constraint.
    """
    fallback_chain = [image_size]
    for size in ["2K", "1K"]:
        if size != image_size and size not in fallback_chain:
            fallback_chain.append(size)
    fallback_chain.append(None)  # last resort: let Gemini decide

    last_exc = None
    for size in fallback_chain:
        img_config = types.ImageConfig(aspect_ratio=aspect_ratio)
        if size:
            img_config = types.ImageConfig(aspect_ratio=aspect_ratio, image_size=size)
        config = types.GenerateContentConfig(
            response_modalities=["IMAGE", "TEXT"],
            image_config=img_config,
        )
        try:
            response = client.models.generate_content(
                model=model, contents=contents, config=config
            )
            if size != image_size:
                logger.info("Gemini succeeded with fallback size=%s", size)
            break
        except Exception as exc:
            logger.warning(
                "Gemini %s failed (size=%s): %s",
                model,
                size,
                exc,
            )
            last_exc = exc
    else:
        raise last_exc  # type: ignore[misc]

    for part in response.parts:
        if part.inline_data is not None:
            return part.as_image().image_bytes

    raise Exception("No image generated by Gemini")


async def generate_thumbnail(
    prompt: str,
    reference_images: list[bytes],
    personal_photos: list[bytes],
    logos: list[bytes] | None = None,
) -> bytes:
    client = genai.Client(api_key=settings.gemini_api_key)

    contents = []

    # Add reference thumbnails
    if reference_images:
        contents.append("Here are my reference thumbnails for style inspiration:")
        for img_bytes in reference_images:
            contents.append(
                types.Part.from_bytes(data=img_bytes, mime_type=_detect_mime(img_bytes))
            )

    # Add channel logo(s)
    if logos:
        contents.append(
            "This is the channel logo. Place it in the EXACT same position "
            "and size as shown in the reference thumbnails. Use this actual "
            "image — do NOT recreate or write the logo text:"
        )
        for logo_bytes in logos:
            contents.append(
                types.Part.from_bytes(
                    data=logo_bytes, mime_type=_detect_mime(logo_bytes)
                )
            )

    # Add personal photos
    if personal_photos:
        contents.append(
            "Here are my personal photos. Pick the best one and place it in "
            "the thumbnail EXACTLY as it appears — do NOT modify, redraw, "
            "regenerate, or alter my face or body in any way. Use the ORIGINAL "
            "photo as-is, only adjusting size and position to fit the layout."
        )
        for img_bytes in personal_photos:
            contents.append(
                types.Part.from_bytes(data=img_bytes, mime_type="image/jpeg")
            )

    # Add the generation prompt
    contents.append(prompt)

    response = client.models.generate_content(
        model="gemini-3-pro-image-preview",
        contents=contents,
        config=types.GenerateContentConfig(
            response_modalities=["IMAGE", "TEXT"],
            image_config=types.ImageConfig(
                aspect_ratio="16:9",
                image_size="4K",
            ),
        ),
    )

    for part in response.parts:
        if part.inline_data is not None:
            image = part.as_image()
            return image.image_bytes

    raise Exception("No image generated by Nano Banana")


async def generate_background(
    prompt: str,
    reference_images: list[bytes],
    logos: list[bytes] | None = None,
    previous_image: bytes | None = None,
    aspect_ratio: str = "16:9",
    image_size: str = "4K",
    model: str = "gemini-3-pro-image-preview",
) -> bytes:
    """Generate ONLY background + logo, no person, no text."""
    client = genai.Client(api_key=settings.gemini_api_key)

    contents = []

    if reference_images:
        contents.append(
            "Reference thumbnails — use ONLY for layout and logo placement. "
            "Ignore their text and people (those are added in later steps). "
            "Generate visuals based on the TOPIC below, not the references."
        )
        for img_bytes in reference_images:
            contents.append(
                types.Part.from_bytes(data=img_bytes, mime_type=_detect_mime(img_bytes))
            )

    if logos:
        contents.append(
            "This is the channel logo. Place it in the same position and size "
            "as in the reference thumbnails."
        )
        for logo_bytes in logos:
            contents.append(
                types.Part.from_bytes(
                    data=logo_bytes, mime_type=_detect_mime(logo_bytes)
                )
            )

    if previous_image:
        contents.append(
            "Current background (user wants changes). Keep everything unchanged except what they asked for:"
        )
        contents.append(
            types.Part.from_bytes(
                data=previous_image, mime_type=_detect_mime(previous_image)
            )
        )

    contents.append(prompt)

    return _generate_image(client, model, contents, aspect_ratio, image_size)


async def composite_with_effects(
    background_bytes: bytes,
    person_bytes: bytes,
    reference_images: list[bytes],
    extra_instructions: str | None = None,
    previous_image: bytes | None = None,
    aspect_ratio: str = "16:9",
    image_size: str = "4K",
    model: str = "gemini-3-pro-image-preview",
) -> bytes:
    """Use Gemini to composite person onto background with effects matching references."""
    client = genai.Client(api_key=settings.gemini_api_key)

    contents = []

    if reference_images:
        contents.append(
            "Reference thumbnails — study how the person is composited: "
            "position, size, glow, lighting, color grading, edge effects. Replicate this style."
        )
        for img_bytes in reference_images:
            contents.append(
                types.Part.from_bytes(data=img_bytes, mime_type=_detect_mime(img_bytes))
            )

    if previous_image:
        contents.append(
            "Current composite (user wants changes). Keep everything unchanged except what they asked for:"
        )
        contents.append(
            types.Part.from_bytes(
                data=previous_image, mime_type=_detect_mime(previous_image)
            )
        )

    contents.append("Background image (use as-is, do NOT modify):")
    contents.append(
        types.Part.from_bytes(
            data=background_bytes, mime_type=_detect_mime(background_bytes)
        )
    )

    contents.append(
        "Person photo (remove its background, place onto the background above):"
    )
    contents.append(
        types.Part.from_bytes(data=person_bytes, mime_type=_detect_mime(person_bytes))
    )

    instructions = (
        "1. Preserve the person's face/body exactly — no redrawing or distortion\n"
        "2. Remove the person's original background\n"
        "3. Apply reference-style effects (glow, lighting, color grading) to the person\n"
        "4. Position/size the person as in references\n"
        "5. No text. Background must remain pixel-perfect."
    )
    if extra_instructions:
        instructions += f"\n6. User request: {extra_instructions}"
    contents.append(instructions)

    return _generate_image(client, model, contents, aspect_ratio, image_size)


async def add_text_with_style(
    composite_bytes: bytes,
    text: str,
    reference_images: list[bytes],
    previous_image: bytes | None = None,
    extra_instructions: str | None = None,
    aspect_ratio: str = "16:9",
    image_size: str = "4K",
    model: str = "gemini-3-pro-image-preview",
) -> bytes:
    """Use Gemini to add styled text to the composite, matching reference typography."""
    client = genai.Client(api_key=settings.gemini_api_key)

    contents = []

    if reference_images:
        contents.append(
            "Reference thumbnails — replicate the SAME typography: "
            "font style, weight, color, size, position, effects (shadow, stroke, glow)."
        )
        for img_bytes in reference_images:
            contents.append(
                types.Part.from_bytes(data=img_bytes, mime_type=_detect_mime(img_bytes))
            )

    if previous_image:
        contents.append(
            "Previous version (user wants changes). Keep everything except what they asked for:"
        )
        contents.append(
            types.Part.from_bytes(
                data=previous_image, mime_type=_detect_mime(previous_image)
            )
        )

    contents.append("Current thumbnail (add text only, change nothing else):")
    contents.append(
        types.Part.from_bytes(
            data=composite_bytes, mime_type=_detect_mime(composite_bytes)
        )
    )

    text_prompt = (
        f'Add this text: "{text}"\n'
        f"Match reference typography style. Make it readable and impactful.\n"
        f"Do NOT change background, person, logo, or any other element."
    )
    if extra_instructions:
        text_prompt += f"\nUser request: {extra_instructions}"
    contents.append(text_prompt)

    return _generate_image(client, model, contents, aspect_ratio, image_size)
